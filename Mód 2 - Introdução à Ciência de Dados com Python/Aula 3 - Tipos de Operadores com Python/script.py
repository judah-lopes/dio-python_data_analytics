#! ===== etapa 1 - Conhecendo os operadores aritméticos =====
#   
#?  ADIÇÃO
print(4 + 4)
#________________________
#?  SUBTRAÇÃO 
print(4 - 4)
#________________________
#?  MUTIPLICAÇÃO
print(4 * 4)
#________________________
#?  DIVISÃO
print(10 / 4)
#________________________
#?  DIVISÃO sem resto
print(10 // 4)
#________________________
#?  MÓDULO
print(10 % 4)
#________________________
#?  EXPONÊNCIAÇÃO
print(10 ** 4)
